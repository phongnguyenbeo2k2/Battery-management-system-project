void TFT_ST7735_Draw_pixel(TFT_ST7735_Info_t *foo_tft, uint16_t x, uint16_t y, uint16_t color)
{
	if (x >= _width_t || y >= _height_t)
	{
		return;
	}
	TFT_ST7735_Select(foo_tft);
	TFT_ST7735_Set_address_window(foo_tft, x, y, x+1, y+1);
	uint8_t data[] = { color >> 8, color & 0xFF };
	TFT_ST7735_Write_data(foo_tft, data, sizeof(data));
	TFT_ST7735_Unselect(foo_tft);
}

void TFT_ST7735_Write_char(TFT_ST7735_Info_t *foo_tft, uint16_t x, uint16_t y, char ch, TFT_ST7735_FontDef_t font, uint16_t color, uint16_t bgcolor)
{
	uint32_t i, b, j;
	TFT_ST7735_Set_address_window(foo_tft, x, y, x+font.width-1, y+font.height-1);
    for(i = 0; i < font.height; i++) {
        b = font.data[(ch - 32) * font.height + i];
        for(j = 0; j < font.width; j++) {
            if((b << j) & 0x8000)  {
                uint8_t data[] = { color >> 8, color & 0xFF };
                TFT_ST7735_Write_data(foo_tft, data, sizeof(data));
            } else {
                uint8_t data[] = { bgcolor >> 8, bgcolor & 0xFF };
                TFT_ST7735_Write_data(foo_tft, data, sizeof(data));
            }
        }
    }
}

void TFT_ST7735_Write_string(TFT_ST7735_Info_t *foo_tft, uint16_t x, uint16_t y, const char* str, TFT_ST7735_FontDef_t font, uint16_t color, uint16_t bgcolor)
{
	TFT_ST7735_Select(foo_tft);

    while(*str) {
        if(x + font.width >= _width_t) {
            x = 0;
            y += font.height;
            if(y + font.height >= _height_t)
            {
                break;
            }

            if(*str == ' ')
            {
                // skip spaces in the beginning of the new line
                str++;
                continue;
            }
        }

        TFT_ST7735_Write_char(foo_tft, x, y, *str, font, color, bgcolor);
        x += font.width;
        str++;
    }
    TFT_ST7735_Unselect(foo_tft);
}

void TFT_ST7735_Fill_rectangle(TFT_ST7735_Info_t *foo_tft, uint16_t x, uint16_t y, uint16_t w, uint16_t h, uint16_t color)
{
    if((x >= _width_t) || (y >= _height_t)) return;
    if((x + w - 1) >= _width_t) w = _width_t - x;
    if((y + h - 1) >= _height_t) h = _height_t - y;

    TFT_ST7735_Select(foo_tft);
    TFT_ST7735_Set_address_window(foo_tft, x, y, x+w-1, y+h-1);

    uint8_t data[] = { color >> 8, color & 0xFF };
	HAL_GPIO_WritePin(foo_tft->dc_port, foo_tft->dc_pin, GPIO_PIN_SET);

    for(y = h; y > 0; y--) {
        for(x = w; x > 0; x--) {
            HAL_SPI_Transmit(foo_tft->tft_hspi, data, sizeof(data), HAL_MAX_DELAY);
        }
    }

    TFT_ST7735_Unselect(foo_tft);
}
void TFT_ST7735_Draw_image(TFT_ST7735_Info_t *foo_tft, uint16_t x, uint16_t y, uint16_t w, uint16_t h, const uint16_t* data)
{
    if((x >= _width_t) || (y >= _height_t)) return;
    if((x + w - 1) >= _width_t) return;
    if((y + h - 1) >= _height_t) return;

    TFT_ST7735_Select(foo_tft);
    TFT_ST7735_Set_address_window(foo_tft, x, y, x+w-1, y+h-1);
    TFT_ST7735_Write_data(foo_tft,(uint8_t*)data, sizeof(uint16_t)*w*h);
    TFT_ST7735_Unselect(foo_tft);
}

