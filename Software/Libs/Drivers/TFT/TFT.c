void TFT_ST7735_Display_init(TFT_ST7735_Info_t *foo_tft, const uint8_t *addr)
{
    uint8_t numCommands, numArgs;
    uint16_t ms;

    numCommands = *addr++;
    while(numCommands--) {
        uint8_t cmd = *addr++;
        TFT_ST7735_Write_command(foo_tft,cmd);

        numArgs = *addr++;
        // If high bit set, delay follows args
        ms = numArgs & DELAY;
        numArgs &= ~DELAY;
        if(numArgs) {
            TFT_ST7735_Write_data(foo_tft,(uint8_t*)addr, numArgs);
            addr += numArgs;
        }

        if(ms) {
            ms = *addr++;
            if(ms == 255) ms = 500;
            HAL_Delay(ms);
        }
    }
}
void TFT_ST7735_Set_address_window(TFT_ST7735_Info_t *foo_tft, uint8_t x0, uint8_t y0, uint8_t x1, uint8_t y1)
{
	// column address set
	TFT_ST7735_Write_command(foo_tft, ST7735_CASET);
	uint8_t data[] = { 0x00, x0 + _xstart_t, 0x00, x1 + _xstart_t };
	TFT_ST7735_Write_data(foo_tft, data, sizeof(data));

	// row address set
	TFT_ST7735_Write_command(foo_tft, ST7735_RASET);
	data[1] = y0 + _ystart_t;
	data[3] = y1 + _ystart_t;
	TFT_ST7735_Write_data(foo_tft, data, sizeof(data));
    // write to RAM
	TFT_ST7735_Write_command(foo_tft, ST7735_RAMWR);
}

